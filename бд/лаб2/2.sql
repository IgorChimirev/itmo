SELECT 
    "Н_ЛЮДИ"."ОТЧЕСТВО",
    "Н_ВЕДОМОСТИ"."ИД",
    "Н_СЕССИЯ"."ДАТА"
FROM 
    "Н_ЛЮДИ"
RIGHT JOIN 
    "Н_ВЕДОМОСТИ" ON "Н_ЛЮДИ"."ИД" = "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
RIGHT JOIN 
    "Н_СЕССИЯ" ON "Н_ВЕДОМОСТИ"."СЭС_ИД" = "Н_СЕССИЯ"."СЭС_ИД"
WHERE 
    "Н_ЛЮДИ"."ОТЧЕСТВО" = 'Александрович'
    AND "Н_ВЕДОМОСТИ"."ИД" = 1250972
    AND "Н_СЕССИЯ"."ИД" < 1975;