SELECT 
    Л1."ФАМИЛИЯ", 
    Л1."ИМЯ", 
    Л1."ОТЧЕСТВО", 
    Л1."ДАТА_РОЖДЕНИЯ"
FROM 
    "Н_ЛЮДИ" Л1
WHERE 
    EXISTS (
        SELECT 1
        FROM "Н_ЛЮДИ" Л2
        WHERE 
            Л1."ФАМИЛИЯ" = Л2."ФАМИЛИЯ" 
            AND Л1."ДАТА_РОЖДЕНИЯ" <> Л2."ДАТА_РОЖДЕНИЯ"
    )
ORDER BY 
    Л1."ФАМИЛИЯ", 
    Л1."ДАТА_РОЖДЕНИЯ";